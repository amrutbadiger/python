'''
 to convert all the elements of a list to lowercase
 ,uppercase or capitalize the first letter
 using a for loop and dispaying all the elements.
 '''
name =["Bob", "rAm", "SId", "raJ", "JACK"]
for a_name in name:
    a_name=a_name.lower()
    print(a_name)
for a_name in name:
    a_name=a_name.upper()
    print(a_name)
for a_name in name:
    a_name=a_name.title()
    print(a_name)
